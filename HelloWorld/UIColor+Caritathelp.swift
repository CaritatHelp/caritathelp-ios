import UIKit

extension UIColor {
    class func greenCaritathelp() -> UIColor {
        return UIColor(red: 83.0/255.0, green: 166.0/255.0, blue: 82.0/255.0, alpha: 1.0)
    }
    
    class func grayCaritathelp() -> UIColor {
        return UIColor(red: 218.0/255.0, green: 218.0/255.0, blue: 218.0/255.0, alpha: 1.0)
    }
    
    class func warmGreyColor() -> UIColor {
        return UIColor(white: 144.0 / 255.0, alpha: 1.0)
    }
    
    class func lightGreenCaritathelp() -> UIColor {
        return UIColor(red: 213.0/255.0, green: 231.0/255.0, blue: 207.0/255.0, alpha: 1.0)
    }
    
    class func GreenBasicCaritathelp() -> UIColor {
        return UIColor(red: 48.0/255.0, green: 160.0/255.0, blue: 28.0/255.0, alpha: 1.0)
    }
}
